package trivzia.jnas.luckydraw.bo;

public class Billing
{
	private int gold;
	private int silver;
	private int plustime;
	private int lifeline;
	private int gamepoint;
	private int totalEarning;

	public int getGold()
	{
		return gold;
	}

	public void setGold(int gold)
	{
		this.gold = gold;
	}

	public int getSilver()
	{
		return silver;
	}

	public void setSilver(int silver)
	{
		this.silver = silver;
	}

	public int getPlustime()
	{
		return plustime;
	}

	public void setPlustime(int plustime)
	{
		this.plustime = plustime;
	}

	public int getLifeline()
	{
		return lifeline;
	}

	public void setLifeline(int lifeline)
	{
		this.lifeline = lifeline;
	}

	public int getGamepoint()
	{
		return gamepoint;
	}

	public void setGamepoint(int gamepoint)
	{
		this.gamepoint = gamepoint;
	}

	public int getTotalEarning()
	{
		return totalEarning;
	}

	public void setTotalEarning(int totalEarning)
	{
		this.totalEarning = totalEarning;
	}
}
